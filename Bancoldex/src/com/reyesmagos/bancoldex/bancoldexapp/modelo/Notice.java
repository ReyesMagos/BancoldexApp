package com.reyesmagos.bancoldex.bancoldexapp.modelo;

public class Notice {
	
	public String department;
	public String sector;
	public String date;
	public String gremio;
	public String countrie;
	public String title;
	public String content;
	
	
	
	
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getGremio() {
		return gremio;
	}
	public void setGremio(String gremio) {
		this.gremio = gremio;
	}
	public String getCountrie() {
		return countrie;
	}
	public void setCountrie(String countrie) {
		this.countrie = countrie;
	}
	
	
	
	
	

}
